export interface PaystackInitializeRequest {
  email: string;
  amount: number; // Amount in kobo (pesewas for GHS)
  currency?: string;
  reference?: string;
  metadata?: Record<string, any>;
  callback_url?: string;
}

export interface PaystackInitializeResponse {
  status: boolean;
  message: string;
  data: {
    authorization_url: string;
    access_code: string;
    reference: string;
  };
}

